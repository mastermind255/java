package javaprogram;

public class Unaryop {

	public static void main(String[] args) {
		int a=6,y;
		y=a--;
		System.out.println(y);
//		System.out.println(a);
		y=--a;
		System.out.println(y);
//		System.out.println(a);
		y=a++;
		System.out.println(y);
//		System.out.println(a);
		y=++a;
		System.out.println(y);
//		System.out.println(a);

	}

}
