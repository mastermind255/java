package javaprogram;

public class Stringexample2 {

	public static void main(String[] args) {
		System.out.println("HI".compareTo("HI"));
		System.out.println("HI.compareTo(HI)"+"HI".compareTo("Hi"));
		System.out.println("abc.compareTo(aca)"+"abc".compareTo("aca"));
		System.out.println("HI.compareToIgnoreCase(Hi)"+"HI".compareToIgnoreCase("Hi"));

	}

}
